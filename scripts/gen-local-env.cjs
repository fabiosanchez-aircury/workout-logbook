#!/usr/bin/env node
/**
 * Generates a local .env file with JWT keys derived from JWT_SECRET.
 * Uses the same algorithm as Supabase local dev — no external dependencies.
 * Run: node scripts/gen-local-env.js
 */

const fs = require('fs')
const crypto = require('crypto')

const JWT_SECRET = process.env.JWT_SECRET ?? 'super-secret-jwt-token-with-at-least-32-characters-long'
const POSTGRES_PASSWORD = process.env.POSTGRES_PASSWORD ?? 'postgres'

function base64url(obj) {
  return Buffer.from(JSON.stringify(obj))
    .toString('base64')
    .replace(/=/g, '')
    .replace(/\+/g, '-')
    .replace(/\//g, '_')
}

function sign(header, payload, secret) {
  const data = `${base64url(header)}.${base64url(payload)}`
  const sig = crypto
    .createHmac('sha256', secret)
    .update(data)
    .digest('base64')
    .replace(/=/g, '')
    .replace(/\+/g, '-')
    .replace(/\//g, '_')
  return `${data}.${sig}`
}

const exp = Math.floor(Date.now() / 1000) + 10 * 365 * 24 * 60 * 60 // 10 years
const header = { alg: 'HS256', typ: 'JWT' }

const anonKey = sign(header, { iss: 'supabase-local', role: 'anon', exp }, JWT_SECRET)
const serviceKey = sign(header, { iss: 'supabase-local', role: 'service_role', exp }, JWT_SECRET)

const env = `# Generated by scripts/gen-local-env.js — do not commit this file
# Re-generate with: node scripts/gen-local-env.js

# ── Database ──────────────────────────────────────────────────────────────────
POSTGRES_PASSWORD=${POSTGRES_PASSWORD}
JWT_SECRET=${JWT_SECRET}

# ── JWT keys (derived from JWT_SECRET above) ──────────────────────────────────
ANON_KEY=${anonKey}
SERVICE_ROLE_KEY=${serviceKey}

# ── Google OAuth (optional — only needed for Google login) ────────────────────
GOOGLE_ENABLED=false
GOOGLE_CLIENT_ID=
GOOGLE_CLIENT_SECRET=
`

fs.writeFileSync('.env', env)
console.log('✓ .env created with local dev JWT keys')
